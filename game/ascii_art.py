    @classmethod
    def create_box(cls, title: str, content: str, width: int = 60) -> str:
        """Create a fancy box around content."""
        lines = content.split('\n')
        box_lines = []
        
        # Top border
        box_lines.append("â•”" + "â•" * (width - 2) + "â•—")
        
        # Title
        if title:
            title_line = f"â•‘ {title.center(width - 4)} â•‘"
            box_lines.append(title_line)
            box_lines.append("â• " + "â•" * (width - 2) + "â•£")
        
        # Content
        for line in lines:
            if line.strip():
                # Truncate or pad line to fit
                if len(line) > width - 4:
                    line = line[:width - 7] + "..."
                else:
                    line = line.ljust(width - 4)
                box_lines.append(f"â•‘ {line} â•‘")
        
        # Bottom border
        box_lines.append("â•š" + "â•" * (width - 2) + "â•")
        
        return '\n'.join(box_lines)
    
